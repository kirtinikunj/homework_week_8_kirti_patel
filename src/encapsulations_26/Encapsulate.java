package encapsulations_26;

public class Encapsulate {

    private String name;
    private int rollno;
    private int age;

    public void setName(String name){
        this.name=name;
    }

    public String getName(){
        return name;
    }

    public int getRollno(){
        return rollno;
    }

    public void setRollno(int rollno){
        this.rollno=rollno;
    }

    public int getAge(){
        return age;
    }

    public void setAge(int age){
        this.age=age;
    }
}
